import SwiftUI

struct ContentView: View {
    @State private var selectedTab: Tab = .house
    
    init() {
        UITabBar.appearance().isHidden = true
    }
    
    var body: some View {
        CustomTabBar(selectedTab: $selectedTab)
            .edgesIgnoringSafeArea(.bottom)
    }
}

